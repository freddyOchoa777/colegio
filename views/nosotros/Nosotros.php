    <div class="objetivos">
      <h2>OBJETIVOS</h2>
      <ul>
        <li>Establecer normas de comportamiento que permitan la convivencia organizada bajo los principios y fundamentos
          de la Institución.</li>
        <li> Brindar una educación que favorezca la vivencia y el testimonio de los valores.</li>
        <li> Contribuir con el proceso de formación integral de los estudiantes.</li>
        <li> Practicar el respeto, la solidaridad, y la estima por cada uno de los miembros que conforman la comunidad
          educativa.</li>
        <li> Promover la cooperación de los Padres de Familia, primeros educadores de los hijos, para que sean
          partícipes activos
          en su formación, como parte de su misión en el hogar.</li>
        <li> Aplicar los derechos y deberes como medio democrático para una efectiva convivencia social.</li>
        <li> Establecer procedimientos que ayuden a solucionar convictos, cuando estos se presenten.</li>
        <li> Proporcionar espacios que permitan el desarrollo de relaciones interpersonales y grupales, basadas en el
          respeto,
          la responsabilidad, el compromiso y la equidad.</li>
      </ul>
