        <p class="col-visión">
          La visión del futuro COLEGIO SAN JOSÉ DE CAFASSO es la de constituirse en una alternativa educativa de la más
          alta
          calidad en el sur de Bogotá organizando un proyecto que articule los elementos fundamentales implícitos en una
          educación integradora; las dimensiones física, mental, emocional y espiritual de cada una de las personas que
          integren
          a nuestra comunidad educativa, realizando esfuerzos por trascender mucho más allá de los límites físicos de la
          institución.
          El colegio San José de Cafasso participa en la tarea de proporcionar un desarrollo integral del individuo
          desde sus
          primeros años de vida, encaminándolo hacia una convivencia que le permita vivenciar los valores en los cuales
          se
